numbers = [5, 6, 7 ,8 ,1]
print(numbers)

numbers.append(99) # menambah angka dibelakang angka terakhir
print(numbers)

numbers.insert(2, 100) # menambah nilai baru 2=letak index dan 100=angka yg akan ditambahkan
print(numbers)

numbers.pop(5) # menghapus elemen yg ada di index ke lima
print(numbers)

numbers.remove(8) # menghapus elemen 8
print(numbers)

numbers.sort() # mengurutkan dari kecil ke besar
print(numbers)